#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;
	size_t	len;

	if (!s1 || !s2)
		return (0);
	len = ft_strlen(s1) + ft_strlen(s2) + 1;
	res = (char *)malloc(len);
	if (res)
	{
		ft_strlcpy(res, s1, len);
		ft_strlcat(res, s2, len);
	}
	return (res);
}
